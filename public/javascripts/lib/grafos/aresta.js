class Aresta {

    constructor(vO,vD,valor) {
        this.vO = vO;
        this.vD = vD;
        this.valor = valor;
    }

    get getVO() {
        return this.vO;
    }

    set setVO(vO) {
        this.vO = vO;
    }

    get getVD() {
        return this.vD;
    }

    set setVD(vD) {
        this.vD = vD;
    }

    get getValor() {
        return this.valor;
    }

    set setValor(valor) {
        this.valor = valor;
    }

}